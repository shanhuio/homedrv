package doorway

import (
	"net/http"
	"net/url"

	"shanhu.io/misc/httputil"
)

func probeURL(ctx C, u *url.URL) error {
	req := &http.Request{
		Method: "GET",
		URL:    u,
	}
	req = req.WithContext(ctx)
	resp, err := http.DefaultClient.Do(req)
	if err != nil {
		return err
	}
	defer resp.Body.Close()

	if resp.StatusCode != 200 {
		return httputil.RespError(resp)
	}
	return nil
}
